package fundamental_of_java;

public class control_structure_1_quiz {
    // pyramid program
public static void main(String[] args) {
        int n=5;

        for(int i=0;i<n; i++){
            for(int j=0; j<i; j++){

                System.out.print("*");
            }
            System.out.println();
        }


}
}
